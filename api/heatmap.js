module.exports = async function handler(request, response) {
  response.setHeader("Access-Control-Allow-Origin", "*");
  response.setHeader("Access-Control-Allow-Methods", "GET, OPTIONS");
  response.setHeader("Access-Control-Allow-Headers", "Content-Type");
  if (request.method === "OPTIONS") { response.status(204).end(); return; }

  try {
    // 抓上市股票
    const twseStocks = await fetchWithTimeout("https://openapi.twse.com.tw/v1/exchangeReport/STOCK_DAY_ALL");
    // 抓上櫃股票
    let otcStocks = [];
    try {
      otcStocks = await fetchWithTimeout("https://www.tpex.org.tw/openapi/v1/tpex_mainboard_daily_close_quotes");
    } catch(e) {}

    const allStocks = [];

    // 處理上市
    for (const s of twseStocks) {
      const code = s.Code || "";
      const name = s.Name || "";
      const change = parseFloat(s.Change) || 0;
      const close = parseFloat(s.ClosingPrice) || 0;
      const value = parseFloat(s.TradeValue) || 0;
      const volume = parseFloat(s.TradeVolume) || 0;
      if (!close || !code) continue;
      allStocks.push({ code, name, change, close, value, volume });
    }

    // 處理上櫃
    for (const s of otcStocks) {
      const code = s.SecuritiesCompanyCode || s.Code || "";
      const name = s.CompanyName || s.Name || "";
      const close = parseFloat(s.Close || s.ClosingPrice) || 0;
      const prevClose = parseFloat(s.PreviousClose || s.YesterdayClosingPrice) || 0;
      const change = prevClose > 0 ? close - prevClose : 0;
      const value = parseFloat(s.TransactionAmount || s.TradeValue) || 0;
      const volume = parseFloat(s.TradingShares || s.TradeVolume) || 0;
      if (!close || !code) continue;
      allStocks.push({ code, name, change, close, value, volume });
    }

    const groups = {};
    for (const stock of allStocks) {
      const { code, name, change, close, value, volume } = stock;
      const sector = SECTOR_MAP[code];
      if (!sector) continue;

      const prev = close - change;
      const pct = prev > 0 ? (change / prev) * 100 : 0;

      if (!groups[sector]) {
        groups[sector] = { name: sector, stocks: [], totalValue: 0, up: 0, down: 0, flat: 0 };
      }
      if (!groups[sector].stocks.find(s => s.code === code)) {
        groups[sector].stocks.push({ code, name, pct, change, close, value, volume });
        groups[sector].totalValue += value;
        if (change > 0) groups[sector].up++;
        else if (change < 0) groups[sector].down++;
        else groups[sector].flat++;
      }
    }

    const result = Object.values(groups)
      .filter(g => g.stocks.length >= 1)
      .map(g => {
        const avgPct = g.stocks.reduce((sum, s) => sum + s.pct, 0) / g.stocks.length;
        const sortedByValue = [...g.stocks].sort((a, b) => b.value - a.value);
        const leader = sortedByValue[0];
        return {
          name: g.name,
          pct: parseFloat(avgPct.toFixed(2)),
          totalValue: parseFloat((g.totalValue / 100000000).toFixed(1)),
          count: g.stocks.length,
          up: g.up,
          down: g.down,
          flat: g.flat,
          leader: leader ? `${leader.name} ${leader.pct >= 0 ? "+" : ""}${leader.pct.toFixed(2)}%` : "--",
          leaderCode: leader?.code || "",
          stocks: sortedByValue.map(s => ({
            code: s.code,
            name: s.name,
            close: s.close,
            change: s.change,
            pct: parseFloat(s.pct.toFixed(2)),
            value: s.value,
            volume: s.volume,
          })),
        };
      })
      .sort((a, b) => b.pct - a.pct);

    response.setHeader("Cache-Control", "s-maxage=120, stale-while-revalidate=300");
    response.status(200).json({ ok: true, updatedAt: new Date().toISOString(), sectors: result });

  } catch (e) {
    response.status(502).json({ ok: false, error: e.message });
  }
};
