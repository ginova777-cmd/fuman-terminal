// api/heatmap.js — 即時股價版（mis.twse.com.tw 批次抓取）

async function fetchWithTimeout(url, options = {}, timeout = 15000) {
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), timeout);
  try {
    const res = await fetch(url, {
      ...options,
      signal: controller.signal,
      headers: {
        "User-Agent": "Mozilla/5.0 (compatible; FumanTerminal/1.0)",
        "Accept": "application/json",
        "Referer": "https://mis.twse.com.tw/",
        ...(options.headers || {}),
      },
    });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    return await res.json();
  } finally {
    clearTimeout(timer);
  }
}

// 把股票代碼分批，每批最多50支
function chunkArray(arr, size) {
  const chunks = [];
  for (let i = 0; i < arr.length; i += size) {
    chunks.push(arr.slice(i, i + size));
  }
  return chunks;
}

// 判斷上市或上櫃（用代碼規則）
function getMarket(code) {
  const n = parseInt(code);
  // 上櫃：3000-3999, 4000-4999, 5000-5999, 6000-6999, 7000-7999, 8000-8999
  // 上市：1000-2999, 9000-9999
  if (n >= 3000 && n <= 8999) return "otc";
  return "tse";
}

// 抓一批股票的即時價格
async function fetchBatch(codes) {
  const query = codes.map(code => `${getMarket(code)}_${code}.tw`).join("|");
  const url = `https://mis.twse.com.tw/stock/api/getStockInfo.jsp?ex_ch=${encodeURIComponent(query)}&json=1&delay=0`;
  try {
    const data = await fetchWithTimeout(url);
    return data?.msgArray || [];
  } catch (e) {
    return [];
  }
}

