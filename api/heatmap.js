module.exports = async function handler(request, response) {
  response.setHeader("Access-Control-Allow-Origin", "*");
  response.setHeader("Access-Control-Allow-Methods", "GET, OPTIONS");
  response.setHeader("Access-Control-Allow-Headers", "Content-Type");

  if (request.method === "OPTIONS") {
    response.status(204).end();
    return;
  }

  try {
    const allCodes = Object.keys(SECTOR_MAP);
    const chunks = chunkArray(allCodes, 50);

    // 並行抓所有批次（最多同時5批）
    const results = [];
    for (let i = 0; i < chunks.length; i += 5) {
      const batch = chunks.slice(i, i + 5);
      const batchResults = await Promise.all(batch.map(fetchBatch));
      results.push(...batchResults.flat());
    }

    // 建立 code → 股票資料的 map
    const stockMap = {};
    for (const item of results) {
      const code = item.c || "";
      if (!code) continue;

      // z = 成交價，y = 昨收，若盤中 z 可能為 "-" 則 fallback 到 y
      const close = parseFloat(item.z !== "-" && item.z ? item.z : item.y) || 0;
      const prev  = parseFloat(item.y) || 0;
      if (!close || !prev) continue;

      const change = close - prev;
      const pct    = (change / prev) * 100;

      // MIS API 的 v 是「張」，1張=1000股，成交額(元) = 張數 * 1000 * 現價
      // 轉成「億元」：/ 100_000_000
      const volumeLot = parseFloat(item.v || 0);       // 張
      const amountYi  = (volumeLot * 1000 * close) / 1e8;

      if (stockMap[code]) continue; // 去重（同代碼可能出現在多個批次）

      stockMap[code] = {
        code,
        name:     (item.n || code).replace(/\s+/g, ""),
        close,
        prev,
        change:   parseFloat(change.toFixed(2)),
        pct:      parseFloat(pct.toFixed(2)),
        amountYi: parseFloat(amountYi.toFixed(2)),
        volume:   volumeLot,
      };
    }

    // 分組
    const groups = {};
    for (const [code, sector] of Object.entries(SECTOR_MAP)) {
      const stock = stockMap[code];
      if (!stock) continue;

      if (!groups[sector]) {
        groups[sector] = {
          name: sector,
          stocks: [],
          totalAmountYi: 0,
          up: 0, down: 0, flat: 0,
        };
      }

      // 避免同代碼重複歸入（如 SECTOR_MAP 中偶有重複鍵）
      if (groups[sector].stocks.find(s => s.code === code)) continue;

      groups[sector].stocks.push(stock);
      groups[sector].totalAmountYi += stock.amountYi;
      if (stock.change > 0)      groups[sector].up++;
      else if (stock.change < 0) groups[sector].down++;
      else                       groups[sector].flat++;
    }

    const sectorList = Object.values(groups)
      .filter(g => g.stocks.length >= 1)
      .map(g => {
        const avgPct       = g.stocks.reduce((sum, s) => sum + s.pct, 0) / g.stocks.length;
        const sortedByAmt  = [...g.stocks].sort((a, b) => b.amountYi - a.amountYi);
        const leader       = sortedByAmt[0];

        return {
          name:       g.name,
          pct:        parseFloat(avgPct.toFixed(2)),
          amountYi:   parseFloat(g.totalAmountYi.toFixed(1)),
          count:      g.stocks.length,
          up:         g.up,
          down:       g.down,
          flat:       g.flat,
          leader:     leader
            ? `${leader.name} ${leader.pct >= 0 ? "+" : ""}${leader.pct.toFixed(2)}%`
            : "--",
          leaderCode: leader?.code || "",
          stocks:     sortedByAmt.map(s => ({
            code:     s.code,
            name:     s.name,
            close:    s.close,
            prev:     s.prev,
            change:   s.change,
            pct:      s.pct,
            amountYi: s.amountYi,
            volume:   s.volume,
          })),
        };
      })
      .sort((a, b) => b.pct - a.pct);

    response.setHeader("Cache-Control", "s-maxage=60, stale-while-revalidate=120");
    response.status(200).json({
      ok:        true,
      source:    "MIS即時",
      updatedAt: new Date().toISOString(),
      sectors:   sectorList,
    });

  } catch (e) {
    console.error("[heatmap]", e);
    response.status(502).json({ ok: false, error: e.message });
  }
};
