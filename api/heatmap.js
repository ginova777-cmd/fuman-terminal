module.exports = async function handler(request, response) {
  response.setHeader("Access-Control-Allow-Origin", "*");
  response.setHeader("Access-Control-Allow-Methods", "GET, OPTIONS");
  response.setHeader("Access-Control-Allow-Headers", "Content-Type");
  if (request.method === "OPTIONS") { response.status(204).end(); return; }

  try {
    const stocks = await fetchWithTimeout("https://openapi.twse.com.tw/v1/exchangeReport/STOCK_DAY_ALL");

    const groups = {};
    for (const stock of stocks) {
      const code = stock.Code || "";
      const name = stock.Name || "";
      const change = parseFloat(stock.Change) || 0;
      const close = parseFloat(stock.ClosingPrice) || 0;
      const value = parseFloat(stock.TradeValue) || 0;
      const volume = parseFloat(stock.TradeVolume) || 0;
      if (!close || !code) continue;

      const prev = close - change;
      const pct = prev > 0 ? (change / prev) * 100 : 0;
      const sector = SECTOR_MAP[code];
      if (!sector) continue;

      if (!groups[sector]) {
        groups[sector] = { name: sector, stocks: [], totalValue: 0, up: 0, down: 0, flat: 0 };
      }
      groups[sector].stocks.push({ code, name, pct, change, close, value, volume });
      groups[sector].totalValue += value;
      if (change > 0) groups[sector].up++;
      else if (change < 0) groups[sector].down++;
      else groups[sector].flat++;
    }

    const result = Object.values(groups)
      .filter(g => g.stocks.length >= 2)
      .map(g => {
        const avgPct = g.stocks.reduce((sum, s) => sum + s.pct, 0) / g.stocks.length;
        const sortedByValue = [...g.stocks].sort((a, b) => b.value - a.value);
        const leader = sortedByValue[0];
        return {
          name: g.name,
          pct: parseFloat(avgPct.toFixed(2)),
          totalValue: parseFloat((g.totalValue / 100000000).toFixed(1)),
          count: g.stocks.length,
          up: g.up,
          down: g.down,
          flat: g.flat,
          leader: leader ? `${leader.name} ${leader.pct >= 0 ? "+" : ""}${leader.pct.toFixed(2)}%` : "--",
          leaderCode: leader?.code || "",
          // ★ 新增：回傳個股清單供 modal 使用
          stocks: sortedByValue.map(s => ({
            code: s.code,
            name: s.name,
            close: s.close,
            change: s.change,
            pct: s.pct,
            value: s.value,
            volume: s.volume,
          })),
        };
      })
      .sort((a, b) => b.pct - a.pct);

    response.setHeader("Cache-Control", "s-maxage=120, stale-while-revalidate=300");
    response.status(200).json({ ok: true, updatedAt: new Date().toISOString(), sectors: result });

  } catch (e) {
    response.status(502).json({ ok: false, error: e.message });
  }
};
